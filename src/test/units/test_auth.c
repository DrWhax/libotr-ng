static void test_rsig_calculate_c() {
  const char *msg = "hey";
  uint8_t expected_c[ED448_SCALAR_BYTES] = {
      0xfc, 0x2c, 0x80, 0x23, 0x07, 0x13, 0xb5, 0xd4, 0x34, 0x7e, 0x6e, 0x9a,
      0x41, 0x84, 0xb5, 0x55, 0x65, 0x4f, 0x90, 0x29, 0xd4, 0x2f, 0x7c, 0x59,
      0x1e, 0xc1, 0xcd, 0xb6, 0xfb, 0xbf, 0x6b, 0xf4, 0xa7, 0xe1, 0x19, 0x8a,
      0xa4, 0x7a, 0x02, 0x5c, 0x18, 0xd7, 0x36, 0x51, 0xf2, 0xed, 0x5f, 0xab,
      0x9e, 0x15, 0x45, 0x2c, 0x38, 0x2d, 0x31, 0x11,
  };

  otrng_keypair_s a1, a2, a3, t1, t2, t3;
  uint8_t sym1[ED448_PRIVATE_BYTES] = {1}, sym2[ED448_PRIVATE_BYTES] = {2},
          sym3[ED448_PRIVATE_BYTES] = {3}, sym4[ED448_PRIVATE_BYTES] = {4},
          sym5[ED448_PRIVATE_BYTES] = {5}, sym6[ED448_PRIVATE_BYTES] = {6};

  otrng_assert_is_success(otrng_keypair_generate(&a1, sym1));
  otrng_assert_is_success(otrng_keypair_generate(&a2, sym2));
  otrng_assert_is_success(otrng_keypair_generate(&a3, sym3));
  otrng_assert_is_success(otrng_keypair_generate(&t1, sym4));
  otrng_assert_is_success(otrng_keypair_generate(&t2, sym5));
  otrng_assert_is_success(otrng_keypair_generate(&t3, sym6));

  goldilocks_448_scalar_p c;
  otrng_assert_is_success(otrng_rsig_calculate_c_with_usage_and_domain(
      OTRNG_PROTOCOL_USAGE_AUTH, OTRNG_PROTOCOL_DOMAIN_SEPARATION, c, a1.pub,
      a2.pub, a3.pub, t1.pub, t2.pub, t3.pub, (const uint8_t *)msg,
      strlen(msg)));

  uint8_t ser_c[ED448_SCALAR_BYTES] = {0};
  goldilocks_448_scalar_encode(ser_c, c);
  otrng_assert_cmpmem(expected_c, ser_c, ED448_SCALAR_BYTES);
}

static void test_rsig_auth() {
  const char *msg = "hi";

  otrng_keypair_s p1, p2, p3;
  uint8_t sym1[ED448_PRIVATE_BYTES] = {0}, sym2[ED448_PRIVATE_BYTES] = {0},
          sym3[ED448_PRIVATE_BYTES] = {0};

  random_bytes(sym1, ED448_PRIVATE_BYTES);
  random_bytes(sym2, ED448_PRIVATE_BYTES);
  random_bytes(sym3, ED448_PRIVATE_BYTES);

  otrng_assert_is_success(otrng_keypair_generate(&p1, sym1));
  otrng_assert_is_success(otrng_keypair_generate(&p2, sym2));
  otrng_assert_is_success(otrng_keypair_generate(&p3, sym3));

  ring_sig_s dst;
  otrng_assert_is_error(
      otrng_rsig_authenticate(&dst, p1.priv, p1.pub, p2.pub, p3.pub, p2.pub,
                              (unsigned char *)msg, strlen(msg)));

  otrng_assert_is_error(
      otrng_rsig_authenticate(&dst, p1.priv, p1.pub, p1.pub, p3.pub, p1.pub,
                              (unsigned char *)msg, strlen(msg)));

  otrng_assert_is_success(
      otrng_rsig_authenticate(&dst, p1.priv, p1.pub, p1.pub, p2.pub, p3.pub,
                              (unsigned char *)msg, strlen(msg)));

  otrng_assert(otrng_rsig_verify(&dst, p1.pub, p2.pub, p3.pub,
                                 (unsigned char *)msg, strlen(msg)));

  otrng_assert_is_success(
      otrng_rsig_authenticate(&dst, p1.priv, p1.pub, p3.pub, p1.pub, p2.pub,
                              (unsigned char *)msg, strlen(msg)));

  otrng_assert(otrng_rsig_verify(&dst, p3.pub, p1.pub, p2.pub,
                                 (unsigned char *)msg, strlen(msg)));
}

